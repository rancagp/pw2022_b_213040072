<?php
// Pengulangan
// for
// While
// do.. while
// foreach : pengulangan khusus array

// Pengulangan 1
for ($i = 0; $i < 5; $i++) {
    echo "Hello World!<br>";
}

// Pengulangan 2
while ($i < 5) {
    echo "Hello World! <br>";
    $i++;
}

// Pengulangan 3
$i = 10;
do {
    echo "Hello World! <br>";
    $i++;
} while ($i < 5);
